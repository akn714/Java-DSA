public class MathsClass {
    public static void main(String[] args) {
        // max
        System.out.println(Math.max(5,2));
        
        // min
        System.out.println(Math.min(5,2));

        // random
        System.out.println(Math.random());
        System.out.println((int)(Math.random()*100));
    }
}
